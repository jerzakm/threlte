<script lang="ts">
  import { HierarchicalObject, T } from '@threlte/core'
  import { onDestroy } from 'svelte'
  import { Group } from 'three'
  import { useRoot } from '../../Root/useRoot'
  import type { NodeProps } from '../../lib/props'
  import { createNodeContext } from '../createNodeContext'

  type $$Props = NodeProps & {
    order?: number
    cls?: string
  }

  export let order: $$Props['order'] = undefined
  export let cls: $$Props['cls'] = undefined

  const {
    scaleFactor,
    onEvent,
    addNode,
    removeNode,
    updateNodeProps,
    mainAxis,
    crossAxis,
    depthAxis,
    classParser
  } = useRoot()

  export const group = new Group()
  export const contentGroup = new Group()

  // identify this node as an item
  group.userData.isItem = true

  export const { node } = createNodeContext(order)

  $: parsedProps = classParser ? classParser(cls || '') : {}

  addNode(node, group, $$restProps as NodeProps, 'item')
  updateNodeProps(node, { ...$$restProps, ...parsedProps } as NodeProps, true)
  $: updateNodeProps(node, { ...$$restProps, ...parsedProps } as NodeProps)
  onDestroy(() => {
    removeNode(node)
  })

  let computedWidth = 1
  let computedHeight = 1

  // after the parent has been reflowed, we can use the calculated layout to set the properties of the box
  onEvent('reflow:after', () => {
    computedWidth =
      typeof $$restProps.width === 'number'
        ? $$restProps.width
        : node.getComputedWidth() / $scaleFactor

    computedHeight =
      typeof $$restProps.height === 'number'
        ? $$restProps.height
        : node.getComputedHeight() / $scaleFactor

    contentGroup.position[$mainAxis] = computedWidth / 2
    contentGroup.position[$crossAxis] = -computedHeight / 2
    contentGroup.position[$depthAxis] = 0
  })
</script>

<T is={group}>
  <T is={contentGroup} />
</T>

<HierarchicalObject
  onChildMount={(child) => {
    if (child.userData.isItem) {
      group.add(child)
    } else {
      contentGroup.add(child)
    }
  }}
  onChildDestroy={(child) => {
    if (child.userData.isItem) {
      group.remove(child)
    } else {
      contentGroup.remove(child)
    }
  }}
>
  <slot
    width={computedWidth}
    height={computedHeight}
  />
</HierarchicalObject>
